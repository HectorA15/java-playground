package tarea.simulacion.tema4.MonteCarlo;

public class Clavo {
    private double x;
    private double y;
    private double radio;

    public Clavo(double x, double y, double radio) {
        this.x = x;
        this.y = y;
        this.radio = radio;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getRadio() {
        return radio;
    }
}